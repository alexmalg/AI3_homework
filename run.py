#!/usr/bin/env python3
# ============================================================================
# run.py - Точка входа для запуска приложения
# ============================================================================
# Этот файл является главной точкой входа для приложения.
# Запустить приложение можно командой:
#     python run.py
#
# Что он делает:
# 1. Добавляет текущую директорию в Python path (для импорта локальных модулей)
# 2. Импортирует Flask приложение из src/app.py
# 3. Запускает приложение на localhost:5000
# ============================================================================

import sys
import os

# Добавляем текущую директорию в sys.path
# Это позволяет импортировать модули из папки src/
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

# Импортируем Flask приложение
from src.app import app

if __name__ == "__main__":
    # Запускаем приложение
    app.run(host="0.0.0.0", port=5000, debug=True)
